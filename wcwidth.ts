export function wcwidth(code: number): number {
  if (code >= 0x20 && code <= 0x7E) return 1;
  if (code >= 0xA0 && code <= 0xFF) return 1;
  if (code < 0x20 || (code > 0x7E && code < 0xA0)) {
    return code === 0 ? 0 : -1;
  }

  let lo = 0;
  let hi = WCWIDTH_TABLE.length - 1;
  while (lo <= hi) {
    let mid = Math.floor((lo + hi) / 2);
    let [start, end, width] = WCWIDTH_TABLE[mid];
    if (code < start) {
      hi = mid - 1;
    } else if (code > end) {
      lo = mid + 1;
    } else {
      return width;
    }
  }

  return -1;
}
